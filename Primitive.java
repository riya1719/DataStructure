package Addition;

public class Primitive {
	
	int a ;
	float f;
	boolean b;
	long l;
	double d;
	

	public static void main(String[] args) {
		
		Primitive ref = new Primitive();
		
		System.out.println("Integer = " + ref.a);
		System.out.println("Float = " + ref.f);
		System.out.println("Boolean = " + ref.b);
		System.out.println("Long = " + ref.l);
		System.out.println("Double = "+ ref.d);
		
	}

}
